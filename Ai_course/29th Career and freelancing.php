        let currentQuestion = 1;
        const questions = [
            {
                q: "What is the most effective way to convince a high-paying Upwork client that your AI solution works?",
                options: [
                    "Send a 1-minute video demo showing a similar solution you already built.",
                    "Tell them you graduated from a top university.",
                    "Promise them the lowest price on the platform.",
                ],
                correct: 0,
            },
            {
                q: "On Fiverr, what strategy helps you increase your order value from $50 to $500?",
                options: [
                    "Waiting for the client to offer more",
                    "Offering a 3-tier pricing system (Basic/Standard/Premium)",
                    "Asking for a tip",
                ],
                correct: 1,
            },
            {
                q: "What is the 'STAR' method used for in an AI Portfolio?",
                options: [
                    "Drawing stars in the UI",
                    "A framework for writing technical case studies",
                    "A method for training neural networks",
                ],
                correct: 1,
            },
        ];

        function checkAnswer(index) {
            const qData = questions[currentQuestion - 1];
            const feedback = document.getElementById("feedback");
            
            if (index === qData.correct) {
                feedback.innerHTML = "<span class='text-success animate__animated animate__bounceIn'>✔️ Strategy Validated! Next question...</span>";
                setTimeout(() => {
                    currentQuestion++;
                    if (currentQuestion <= questions.length) {
                        loadQuestion();
                        feedback.innerText = "";
                    } else {
                        feedback.innerHTML = "<span class='text-info animate__animated animate__tada'>🚀 Professional Launch Ready! Preparing Graduation...</span>";
                    }
                }, 1500);
            } else {
                feedback.innerHTML = "<span class='text-danger animate__animated animate__shakeX'>❌ Incorrect. Review the class material above.</span>";
            }
        }

        function loadQuestion() {
            const qData = questions[currentQuestion - 1];
            document.getElementById("question-text").innerText = `Q${currentQuestion}: ${qData.q}`;
            const optionsContainer = document.getElementById("options-container");
            optionsContainer.innerHTML = "";
            
            qData.options.forEach((opt, index) => {
                const div = document.createElement("div");
                div.className = "quiz-option";
                div.innerText = opt;
                div.onclick = () => checkAnswer(index);
                optionsContainer.appendChild(div);
            });
        }

        window.onload = loadQuestion;
    </script>

