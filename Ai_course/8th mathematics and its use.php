            <h2 class="topic-header">Class 23: Probability Basics</h2>
            <p>
              Probability is the science of uncertainty, and in AI, it is used
              to help machines make "best guesses" based on available data.
              Since the real world is rarely 100% certain, an AI model uses
              probability to determine the likelihood that an email is spam or
              that a photo contains a cat. We cover the basics of
              <strong>Conditional Probability</strong> and
              <strong>Bayes' Theorem</strong>, which allow AI to update its
              beliefs as new information arrives. For instance, if an AI sees a
              "tail," it updates the probability that the animal is a dog rather
              than a bird. Understanding these rules helps you build models that
              can handle noisy or incomplete data without crashing. At Inspire
              Tech, we focus on the practical side of probability, showing you
              how machines "reason" under pressure. This mathematical layer is
              what gives AI its human-like ability to predict the future.
