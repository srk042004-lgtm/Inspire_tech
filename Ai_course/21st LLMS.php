      let currentQuestion = 1;
      const questions = [
        {
          q: "What is the core mechanism in a Transformer that allows it to weigh the importance of different words in a sentence?",
          options: ["Self-Attention", "Max Pooling", "Backpropagation"],
          correct: 0,
        },
        {
          q: "Which technique allows an AI to generate answers based on a specific, private set of documents?",
          options: [
            "One-Hot Encoding",
            "RAG (Retrieval-Augmented Generation)",
            "K-Means Clustering",
          ],
          correct: 1,
        },
        {
          q: "What metric is used to measure how many of the model's positive predictions were actually correct?",
          options: ["Recall", "Precision", "Bias"],
          correct: 1,
        },
      ];

      function checkAnswer(isCorrect) {
        const feedback = document.getElementById("feedback");
        const bar = document.getElementById("progress-bar");

        if (isCorrect) {
          feedback.innerHTML =
            "<span class='text-success animate__animated animate__flash'>✔️ Correct! AI Knowledge Synced.</span>";
          let progress = (currentQuestion / 3) * 100;
          bar.style.width = progress + "%";

          setTimeout(() => {
            currentQuestion++;
            if (currentQuestion <= 3) {
              loadQuestion();
              feedback.innerText = "";
            } else {
              feedback.innerHTML =
                "<span class='text-info animate__animated animate__tada'>🎓 Congratulations! You've mastered Advanced NLP.</span>";
              document
                .getElementById("nav-container")
                .classList.remove("d-none");
            }
          }, 1200);
        } else {
          feedback.innerHTML =
            "<span class='text-danger animate__animated animate__shakeX'>❌ Incorrect! Recalibrating logic... try again.</span>";
          setTimeout(() => {
            currentQuestion = 1;
            bar.style.width = "0%";
            loadQuestion();
            feedback.innerText = "";
          }, 2000);
        }
      }

      function loadQuestion() {
        const qData = questions[currentQuestion - 1];
        document.getElementById("question-text").innerText =
          `Q${currentQuestion}: ${qData.q}`;
        const optionsContainer = document.getElementById("options-container");
        optionsContainer.innerHTML = "";

        qData.options.forEach((opt, index) => {
          const div = document.createElement("div");
          div.className = "quiz-option";
          div.innerText = opt;
          div.onclick = () => checkAnswer(index === qData.correct);
          optionsContainer.appendChild(div);
        });
      }

      loadQuestion();
    </script>

