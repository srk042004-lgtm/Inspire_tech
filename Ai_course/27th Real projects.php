      let currentQuestion = 1;
      const questions = [
        {
          q: "What is the purpose of a '.gitignore' file when uploading an AI project?",
          options: [
            "To prevent heavy models and secret keys from being uploaded publically.",
            "To delete all the code after it is uploaded.",
            "To make the code run faster on GitHub.",
          ],
          correct: 0,
        },
        {
          q: "In an AI portfolio, what is an 'Impact Statement'?",
          options: [
            "The color of the website",
            "A description of the hardware used",
            "Explaining how your project solved a specific real-world problem.",
          ],
          correct: 2,
        },
        {
          q: "Which development methodology involves building a 'Minimum Viable Product' (MVP) first?",
          options: ["Waterfall", "Agile", "Random Coding"],
          correct: 1,
        },
      ];

      function checkAnswer(isCorrect) {
        const feedback = document.getElementById("feedback");
        if (isCorrect) {
          feedback.innerHTML =
            "<span class='text-success animate__animated animate__flash'>✔️ Portfolio Standard Met!</span>";
          setTimeout(() => {
            currentQuestion++;
            if (currentQuestion <= 3) {
              loadQuestion();
              feedback.innerText = "";
            } else {
              feedback.innerHTML =
                "<span class='text-info animate__animated animate__tada'>🚀 Professional Module Complete! Unlock Final Certification.</span>";
              document
                .getElementById("nav-container")
                .classList.remove("d-none");
            }
          }, 1200);
        } else {
          feedback.innerHTML =
            "<span class='text-danger animate__animated animate__shakeX'>❌ Incorrect! Think about security and scale.</span>";
        }
      }

      function loadQuestion() {
        const qData = questions[currentQuestion - 1];
        document.getElementById("question-text").innerText =
          `Q${currentQuestion}: ${qData.q}`;
        const optionsContainer = document.getElementById("options-container");
        optionsContainer.innerHTML = "";
        qData.options.forEach((opt, index) => {
          const div = document.createElement("div");
          div.className = "quiz-option";
          div.innerText = opt;
          div.onclick = () => checkAnswer(index === qData.correct);
          optionsContainer.appendChild(div);
        });
      }

      loadQuestion();
    </script>

